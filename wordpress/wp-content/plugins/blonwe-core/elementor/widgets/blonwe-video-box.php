	protected function render() {
		$settings = $this->get_settings_for_display();
		$target = $settings['btn_link']['is_external'] ? ' target="_blank"' : '';
		$nofollow = $settings['btn_link']['nofollow'] ? ' rel="nofollow"' : '';
		
		$output = '';
		
		echo '<div class="klb-video-box">';
		echo '<div class="klb-video-content">';
		
		if($settings['video_type'] == 'type2'){
			echo '<video autoplay playsinline loop muted src="'.esc_url($settings['video']['url']).'"></video>';
			
		} else {
			echo $settings['video_content'];
		}	
		
		echo '</div><!-- klb-video-content -->';
		echo '<div class="klb-text-content">';
		echo '<div class="text-inner">';
		echo '<h5 class="entry-subtitle color-white">'.esc_html($settings['subtitle']).'</h5>';
		echo '<h2 class="entry-title font-md-66 weight-400">'.esc_html($settings['title']).'</h2>';
		echo '<div class="entry-description">';
		echo '<p>'.esc_html($settings['desc']).'</p>';
		echo '</div>';
		echo '<a href="'.esc_url($settings['btn_link']['url']).'" '.esc_attr($target.$nofollow).' class="btn white outline">'.esc_html($settings['btn_title']).'</a>';
		echo '</div>';
		echo '</div>';
		echo '</div>';
		
	}

